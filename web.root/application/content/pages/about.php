        <h1>Welcome to Terry SoRelle's Online Song Book</h1>

        <h2>What Kind of Music is This?</h2>
        <div style="float:right; margin-right: 1rem; margin-bottom: 1rem" >
            <div><img src="/assets/img/firstGuitar.gif"></div>
            <p style="text-align: center"><em>Terry Age 10</em></p>
        </div>
        <p>The site is dedicated to the preservation of traditional music.  My emphasis is on folk music from the English speaking
        world: United States, England, Scotland, Ireland, Wales and Austrailia, but you'll also find selections from Mexico
            and Latin America; the Carribean region, Africa and many other places.  Blues and early country music pieces are our
            most common offerings. </p>

        <p>But not of my songs are strictly traditional. You will also find songs from popular music, jazz, country-western and
            world music here. I think of them all as folk music, music by and for plain folks.</p>

        <p>To me, a piece qualifies as a folksong if it is easily performed by someone, like me, who is not a trained musicican or
        a world class popular singer.  Whatever their authorship, you can made these songs your own. You can
        swap the a a sing-along or jam session. You can make your own version and pass it along to friends or family</p>

        <p>Most of these songs are in the public domain, but many are currently protected by copyright. In these cases I believe
        inclusion of this music on our site falls under the "fair use" provisions of copyright law. No revenue what-so-ever is
        collected for either the text or performance videos that we include. No commercial advertising is allowed on the site.</p>

        <p>That said, I realize that "fair use" is subject to interpretation. If anyone informs us of their belief that our use
        violates a legitimate copyright claim, we will remove the material immediately.</p>
